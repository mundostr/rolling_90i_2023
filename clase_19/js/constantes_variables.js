/**
 * Elementos básicos de lenguaje
 * Constantes y variables
 * 
 * Una constante o variable, nos permite almacenar UN dato.
 * La identificamos con un nombre, idealmente corto y relacionado a lo que contiene.
*/

// Constante: declaro con la palabra reservada const
const PI = 3.14

// Variable: declaro con la palabra reservada let
let saldo = 200

// console.log nos permite mostrar mensajes en la consola de inspección del navegador
// (Herramientas de desarrollo -> F12)
console.log(PI)
console.log(saldo)
console.log(PI * saldo)